#include <iostream>
using namespace std;
int main()
{
    //addition. Subtraction, multiplication, division are same as addiion.
    //There is another operator, though, called modulus (%) which prints the remainder.
    cout << 5+7 << endl;

    //Order of operation
    /* It basically follows the PEMDAS which means Parentheses, 
    Exponent, Multiplication, Division, Addition, Subtraction.
    */
    cout << (5+7)-1*2/3 << endl;
    return 0;
}